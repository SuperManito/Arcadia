function arcadia_init() {
  print_logo

  # жЈҖжөӢй…ҚзҪ®ж–Үд»¶жҳҜеҗҰеӯҳеңЁпјҢдёҚеӯҳеңЁеҲҷеӨҚеҲ¶дёҖд»Ҫ
  make_dir $ConfigDir
  config_files="config.sh bot.json sync.yml"
  for file in $config_files; do
    if [ ! -s "$ConfigDir/$file" ]; then
      cp -fv "$SampleDir/$file" "$ConfigDir/$file"
    fi
  done

  echo -e "\033[1;34m$(date "+%Y-%m-%d %T")${PLAIN} ----- еҗҜеҠЁж ёеҝғжңҚеҠЎејҖе§Ӣ -----\n"
  cd $BackendDir
  if [ ! -x /usr/bin/npm ]; then
    apt-get install -y --no-install-recommends nodejs npm >/dev/null 2>&1
    npm install --omit=dev
  fi
  cd $SrcDir
  pm2 start ecosystem.config.cjs
  if [[ -z $(grep -E "123456789" ${ConfigDir}/bot.json) ]]; then
    $ArcadiaCmd tgbot start
  fi
  echo -e "\n\033[1;34m$(date "+%Y-%m-%d %T")${PLAIN} ----- еҗҜеҠЁж ёеҝғжңҚеҠЎз»“жқҹ -----\n"

  # extra_init.sh
  if [[ -f $FileCliConf ]] && [[ -f $FileInitExtra ]]; then
    cat $FileCliConf | grep -Eq "^CLI_CONFIG_ENABLE_INIT_EXTRA=[\"\']true[\"\']"
    if [ $? -eq 0 ]; then
      echo -e "\n\033[1;34m$(date "+%Y-%m-%d %T")${PLAIN} ----- иҮӘе®ҡд№үеҲқе§ӢеҢ–и„ҡжң¬ејҖе§Ӣ -----\n"
      source $FileInitExtra
      echo -e "\n\033[1;34m$(date "+%Y-%m-%d %T")${PLAIN} ----- иҮӘе®ҡд№үеҲқе§ӢеҢ–и„ҡжң¬з»“жқҹ -----\n"
    fi
  fi

  echo -e "\n\033[1;34m$(date "+%Y-%m-%d %T")${PLAIN} \033[1;32mArcadia service is working...${PLAIN}\n"
}
